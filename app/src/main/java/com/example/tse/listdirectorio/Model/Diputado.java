public class Diputado {
    private String entidad;
    private String tipo_de_eleccion;
    private int nu_diputado;
    private int distrito;
    private String fraccion;
    private String cabecera;
    private String onomastico;
    private String suplente;
    private String nombre;
    private String curul;
    private String email;

    public String getEntidad() {
        return entidad;
    }

    public void setEntidad(String entidad) {
        this.entidad = entidad;
    }

    public String getTipo_de_eleccion() {
        return tipo_de_eleccion;
    }

    public void setTipo_de_eleccion(String tipo_de_eleccion) {
        this.tipo_de_eleccion = tipo_de_eleccion;
    }

    public int getNu_diputado() {
        return nu_diputado;
    }

    public void setNu_diputado(int nu_diputado) {
        this.nu_diputado = nu_diputado;
    }

    public int getDistrito() {
        return distrito;
    }

    public void setDistrito(int distrito) {
        this.distrito = distrito;
    }

    public String getFraccion() {
        return fraccion;
    }

    public void setFraccion(String fraccion) {
        this.fraccion = fraccion;
    }

    public String getCabecera() {
        return cabecera;
    }

    public void setCabecera(String cabecera) {
        this.cabecera = cabecera;
    }

    public String getOnomastico() {
        return onomastico;
    }

    public void setOnomastico(String onomastico) {
        this.onomastico = onomastico;
    }

    public String getSuplente() {
        return suplente;
    }

    public void setSuplente(String suplente) {
        this.suplente = suplente;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCurul() {
        return curul;
    }

    public void setCurul(String curul) {
        this.curul = curul;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
